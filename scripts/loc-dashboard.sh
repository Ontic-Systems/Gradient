## How to refresh locally

\`\`\`bash
scripts/loc-dashboard.sh             # rewrites docs/loc-dashboard.md
scripts/loc-dashboard.sh --json      # emit raw JSON
scripts/loc-dashboard.sh --check     # exit non-zero if doc is stale
\`\`\`

The CI lane runs \`--check\` on every PR; nightly main runs without
\`--check\` and commits any drift back to main.
MARKDOWN

if [ "${1:-}" = "--check" ]; then
    if [ -f docs/loc-dashboard.md ]; then
        # Compare ignoring the date + commit lines (which always differ).
        if diff -q \
            <(grep -v '^> \*\*Auto-generated' docs/loc-dashboard.md) \
            <(grep -v '^> \*\*Auto-generated' "$TMP") \
            > /dev/null; then
            rm -f "$TMP"
            echo "loc-dashboard.md is up-to-date (ignoring date/commit header)."
            exit 0
        fi
        echo "ERROR: docs/loc-dashboard.md is stale. Run scripts/loc-dashboard.sh and commit." >&2
        diff -u docs/loc-dashboard.md "$TMP" >&2 || true
        rm -f "$TMP"
        exit 1
    else
        echo "ERROR: docs/loc-dashboard.md missing. Run scripts/loc-dashboard.sh and commit." >&2
        rm -f "$TMP"
        exit 1
    fi
fi

mv "$TMP" docs/loc-dashboard.md
echo "Wrote docs/loc-dashboard.md (commit=$COMMIT)."
